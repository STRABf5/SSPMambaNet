        x = x.clone()   # !!! Keep the original input intact for the residual connection later
        x[:, :self.dim_conv3, :, :] = self.partial_conv3_1(x[:, :self.dim_conv3, :, :])
        x[:, self.dim_conv3:self.dim_conv3*2, :, :] = self.partial_conv3_2(x[:, self.dim_conv3:self.dim_conv3*2, :, :])
        x = self.conv(x)
        return x

